/**
 * PUT /api/livia/config - Atualizar configura√ß√µes da Livia
 */
router.put('/livia/config', authMiddleware, async (req, res) => {
  try {
    const clinic_id = req.user?.clinic_id || 1;
    const updateData = req.body;
    
    console.log('üîß Atualizando configura√ß√µes da Livia para clinic_id:', clinic_id);
    console.log('üìù Dados para atualiza√ß√£o:', JSON.stringify(updateData, null, 2));
    
    const supabaseAdmin = createSupabaseClient();
    
    // Primeiro, verificar se existe configura√ß√£o
    const { data: existingConfig } = await supabaseAdmin
      .from('livia_configurations')
      .select('id')
      .eq('clinic_id', clinic_id)
      .single();
    
    let result;
    
    if (existingConfig) {
      // Atualizar configura√ß√£o existente
      const { data: updatedConfig, error } = await supabaseAdmin
        .from('livia_configurations')
        .update({
          ...updateData,
          updated_at: new Date().toISOString()
        })
        .eq('clinic_id', clinic_id)
        .select()
        .single();
      
      if (error) {
        console.error('‚ùå Erro ao atualizar configura√ß√µes da Livia:', error);
        res.status(500).json({ error: 'Erro ao atualizar configura√ß√µes', details: error.message });
        return;
      }
      
      result = updatedConfig;
    } else {
      // Criar nova configura√ß√£o
      const { data: createdConfig, error } = await supabaseAdmin
        .from('livia_configurations')
        .insert({
          clinic_id: clinic_id,
          ...updateData,
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString()
        })
        .select()
        .single();
      
      if (error) {
        console.error('‚ùå Erro ao criar configura√ß√µes da Livia:', error);
        res.status(500).json({ error: 'Erro ao criar configura√ß√µes', details: error.message });
        return;
      }
      
      result = createdConfig;
    }
    
    console.log('‚úÖ Configura√ß√µes da Livia atualizadas com sucesso');
    res.json({
      success: true,
      data: result
    });
  } catch (error) {
    console.error('‚ùå Erro inesperado ao atualizar configura√ß√µes da Livia:', error);
    res.status(500).json({ error: 'Erro interno do servidor' });
  }
});

